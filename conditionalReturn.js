function isEven(numer){
    if(numer % 2 === 0){
        return true;
    }
    else{
        return false;
    }
}

console.log(isEven(5));
console.log(isEven(85));
console.log(isEven(86));


function isOdd(number){
    if(number%2===0){
        return true;
    }
    return false;
}

console.log(isOdd(12))
console.log(isOdd(55));